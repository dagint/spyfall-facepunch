/**
 * Generate a player avatar — colored circle with initials.
 * Color is deterministic based on the name string.
 */
export function playerAvatar(name, size = 32) {
  const colors = [
    '#22d3ee', '#f43f5e', '#f59e0b', '#10b981',
    '#8b5cf6', '#ec4899', '#06b6d4', '#84cc16',
    '#f97316', '#6366f1', '#14b8a6', '#e11d48',
  ];
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash);
  }
  const color = colors[Math.abs(hash) % colors.length];
  const initials = name.slice(0, 2).toUpperCase();
  const fontSize = size * 0.4;

  return `<svg width="${size}" height="${size}" viewBox="0 0 ${size} ${size}">
    <circle cx="${size / 2}" cy="${size / 2}" r="${size / 2}" fill="${color}" opacity="0.15"/>
    <circle cx="${size / 2}" cy="${size / 2}" r="${size / 2 - 1}" fill="none" stroke="${color}" stroke-width="1.5" opacity="0.4"/>
    <text x="${size / 2}" y="${size / 2}" text-anchor="middle" dominant-baseline="central" fill="${color}" font-size="${fontSize}" font-family="JetBrains Mono, monospace" font-weight="600">${initials}</text>
  </svg>`;
}
